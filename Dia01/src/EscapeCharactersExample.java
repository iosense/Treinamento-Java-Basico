public class EscapeCharactersExample {
    public static void main(String[] args) {
        // Exemplo de uso de caracteres de escape em Java
        
        // Quebra de linha
        System.out.println("Esta é a primeira linha.\nEsta é a segunda linha."); 
        
        // Tabulação
        System.out.println("Primeira coluna\t\tSegunda coluna");
        
        // Backslash
        System.out.println("Este é um backslash: \\");
        
        // Aspas simples
        System.out.println("Esta é uma aspa simples: \'");
        
        // Aspas duplas
        System.out.println("Esta é uma aspa dupla: \"");
    }
}